"""
Module de tests pour OxygenCS.

Ce module contient des tests unitaires pour 
vérifier les fonctionnalités de base de l'application OxygenCS.
"""


def test_addition():
    """Test the addition of two numbers."""
    assert 1 + 1 == 2


def test_subtraction():
    """Test the subtraction of two numbers."""
    assert 5 - 3 == 2


def test_addition_large():
    """Test the addition of two larger numbers."""
    assert 11 + 1 == 12


def test_addition_larger():
    """Test the addition of two even larger numbers."""
    assert 11 + 11 == 22
